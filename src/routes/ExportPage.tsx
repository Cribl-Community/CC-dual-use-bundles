  const packYaml = useMemo(() =>
    generateStreamPack(selectedSource, sourceName, fields, guardPolicy),
    [selectedSource, sourceName, fields, guardPolicy]
  );

  function handleDownloadPack() {
    downloadBlob(packYaml, `${selectedSource}-pipeline.yml`, 'text/yaml');
    setExportStatus('Pipeline YAML downloaded');
  }

  function handleCopyPack() {
    navigator.clipboard.writeText(packYaml).then(() => setExportStatus('Copied to clipboard'));
  }

