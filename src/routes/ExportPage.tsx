function downloadBlob(content: string, filename: string, mimeType: string) {
  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  a.click();
  URL.revokeObjectURL(url);
}

const allSources = dataSources.flatMap(c => c.sources).filter(s => s.status === 'available');

function generateStreamPack(sourceId: string, sourceName: string, fields: FieldEntry[], guardPolicy: GuardPolicy | null): string {
  const droppableFields = fields.filter(f => f.canDrop === 'Yes').map(f => f.field);
  const maskableFields = fields.filter(f => (f.canMask === 'Yes' || f.canMask === 'Sometimes') && f.guardAction === 'Mask').map(f => f.field);
  const redactFields = fields.filter(f => f.guardAction === 'Redact').map(f => f.field);
  const securityFields = fields.filter(f => f.securitySiem === 'Yes').map(f => f.field);
  const obsFields = fields.filter(f => f.observability === 'Yes' || f.observability === 'Sometimes').map(f => f.field);

  let yaml = '';
  yaml += `# ============================================================\n`;
  yaml += `# Cribl Stream Pipeline: ${sourceName}\n`;
  yaml += `# Generated by Dual-Use Bundles App\n`;
  yaml += `# ============================================================\n`;
  yaml += `#\n`;
  yaml += `# WHAT THIS DOES:\n`;
  yaml += `#   This pipeline optimizes ${sourceName} events for cost-efficient\n`;
  yaml += `#   routing. It drops low-value fields, masks sensitive data for\n`;
  yaml += `#   compliance, and prepares events for intelligent routing to\n`;
  yaml += `#   SIEM (reduced) and Lake (full-fidelity).\n`;
  yaml += `#\n`;
  yaml += `# HOW TO USE:\n`;
  yaml += `#   1. In Cribl Stream, go to Processing > Packs\n`;
  yaml += `#   2. Click "Add Pack" > "Import from File"\n`;
  yaml += `#   3. Upload this .yml file (or paste into a new pipeline)\n`;
  yaml += `#   4. Attach the pipeline to your ${sourceName} route\n`;
  yaml += `#   5. Preview with sample data before enabling in production\n`;
  yaml += `#\n`;
  yaml += `# HOW TO CUSTOMIZE:\n`;
  yaml += `#   - To KEEP a field that's being dropped: remove it from the\n`;
  yaml += `#     Eval function's "remove" list in Section 1\n`;
  yaml += `#   - To DROP additional fields: add them to the "remove" list\n`;
  yaml += `#   - To CHANGE masking behavior: edit the Mask function in\n`;
  yaml += `#     Section 2 (change matchRegex or method)\n`;
  yaml += `#   - To SKIP Guard entirely: delete or disable Section 2\n`;
  yaml += `#   - To ADJUST routing: modify the filter expressions in\n`;
  yaml += `#     Section 3's Route function\n`;
  yaml += `#\n`;
  yaml += `# FIELDS ANALYSIS:\n`;
  yaml += `#   Total fields: ${fields.length}\n`;
  yaml += `#   Droppable: ${droppableFields.length} (${fields.length > 0 ? Math.round(droppableFields.length / fields.length * 100) : 0}%)\n`;
  yaml += `#   Security SIEM required: ${securityFields.length}\n`;
  yaml += `#   Observability required: ${obsFields.length}\n`;
  yaml += `#   Guard protected: ${maskableFields.length + redactFields.length}\n`;
  yaml += `# ============================================================\n\n`;

  yaml += `id: ${sourceId}_optimization\n`;
  yaml += `name: "${sourceName} - Optimization Pipeline"\n`;
  yaml += `description: "Drop low-value fields, mask sensitive data, prepare for routing"\n`;
  yaml += `disabled: false\n`;
  yaml += `groups: {}\n\n`;
  yaml += `functions:\n\n`;

  // Section 1: Field Drops
  yaml += `  # ----------------------------------------------------------\n`;
  yaml += `  # SECTION 1: FIELD DROPS\n`;
  yaml += `  # Remove fields that add volume without analytical value.\n`;
  yaml += `  # These fields are safe to drop for both security and\n`;
  yaml += `  # observability use cases. If your workflow needs a field\n`;
  yaml += `  # listed here, simply remove it from the list below.\n`;
  yaml += `  # ----------------------------------------------------------\n`;
  if (droppableFields.length > 0) {
    yaml += `  - id: drop_low_value_fields\n`;
    yaml += `    filter: "true"\n`;
    yaml += `    disabled: false\n`;
    yaml += `    conf:\n`;
    yaml += `      remove:\n`;
    droppableFields.forEach(f => {
      yaml += `        - ${f}\n`;
    });
    yaml += `    description: "Remove ${droppableFields.length} low-value fields (debug, redundant, raw payloads)"\n`;
    yaml += `    # To keep a specific field: delete its line from the list above\n\n`;
  } else {
    yaml += `  # No droppable fields identified for this source.\n`;
    yaml += `  # All fields carry analytical value for at least one use case.\n\n`;
  }

  // Section 2: Guard / Masking
  yaml += `  # ----------------------------------------------------------\n`;
  yaml += `  # SECTION 2: DATA PROTECTION (Cribl Guard)\n`;
  yaml += `  # Mask or redact sensitive fields before they reach\n`;
  yaml += `  # destinations. This runs AFTER drops so Guard only\n`;
  yaml += `  # processes fields that survived Section 1.\n`;
  yaml += `  #\n`;
  if (guardPolicy) {
    yaml += `  # Compliance: ${guardPolicy.complianceFrameworks.join(', ') || 'General data governance'}\n`;
    yaml += `  # Estimated sensitive: ~${guardPolicy.estimatedSensitivePercent}% of fields\n`;
  }
  yaml += `  # ----------------------------------------------------------\n`;

  if (maskableFields.length > 0) {
    yaml += `  - id: guard_mask_pii\n`;
    yaml += `    filter: "true"\n`;
    yaml += `    disabled: false\n`;
    yaml += `    conf:\n`;
    yaml += `      rules:\n`;
    maskableFields.forEach(f => {
      yaml += `        - matchRegex: /.*/\n`;
      yaml += `          fieldName: ${f}\n`;
      yaml += `          method: mask\n`;
      yaml += `          # Replaces with ****. Change to 'hash' for reversible pseudonymization\n`;
    });
    yaml += `    description: "Mask ${maskableFields.length} fields containing PII (IPs, usernames, emails)"\n`;
    yaml += `    # To change masking method: replace 'mask' with 'hash' (SHA-256)\n`;
    yaml += `    # or 'encrypt' (reversible with key management)\n\n`;
  }

  if (redactFields.length > 0) {
    yaml += `  - id: guard_redact_secrets\n`;
    yaml += `    filter: "true"\n`;
    yaml += `    disabled: false\n`;
    yaml += `    conf:\n`;
    yaml += `      rules:\n`;
    redactFields.forEach(f => {
      yaml += `        - matchRegex: /.*/\n`;
      yaml += `          fieldName: ${f}\n`;
      yaml += `          method: redact\n`;
      yaml += `          # Completely removes value. Use for credentials, tokens, secrets\n`;
    });
    yaml += `    description: "Redact ${redactFields.length} fields containing secrets/credentials"\n`;
    yaml += `    # Redacted fields are irrecoverable — ensure you don't need the values\n\n`;
  }

  if (maskableFields.length === 0 && redactFields.length === 0) {
    yaml += `  # No fields flagged for masking/redaction in this source.\n\n`;
  }

  // Section 3: Routing Preparation
  yaml += `  # ----------------------------------------------------------\n`;
  yaml += `  # SECTION 3: ROUTING METADATA\n`;
  yaml += `  # Add a __destination field that downstream routes can use\n`;
  yaml += `  # to split traffic. This doesn't route by itself — it tags\n`;
  yaml += `  # the event so your Route table can filter on it.\n`;
  yaml += `  #\n`;
  yaml += `  # Routing strategy:\n`;
  yaml += `  #   - Security events (${securityFields.length} fields) → SIEM\n`;
  yaml += `  #   - Observability events (${obsFields.length} fields) → Obs platform\n`;
  yaml += `  #   - All events (full fidelity) → Cribl Lake\n`;
  yaml += `  # ----------------------------------------------------------\n`;
  yaml += `  - id: add_routing_metadata\n`;
  yaml += `    filter: "true"\n`;
  yaml += `    disabled: false\n`;
  yaml += `    conf:\n`;
  yaml += `      add:\n`;
  yaml += `        - name: __cribl_route\n`;
  yaml += `          value: "'lake'"\n`;
  yaml += `          # Default: everything goes to Lake. Override below for SIEM/Obs.\n`;
  yaml += `    description: "Tag events with routing metadata for downstream Route table"\n\n`;

  // Section 4: Summary Comment
  yaml += `# ============================================================\n`;
  yaml += `# NEXT STEPS AFTER IMPORT:\n`;
  yaml += `#\n`;
  yaml += `# 1. PREVIEW: Use Stream's "Preview" with sample ${sourceName}\n`;
  yaml += `#    data to verify field drops and masking look correct.\n`;
  yaml += `#\n`;
  yaml += `# 2. ROUTE TABLE: Create routes that filter on __cribl_route:\n`;
  yaml += `#    - Route to SIEM: filter "securityRelevant == true"\n`;
  yaml += `#    - Route to Obs: filter "obsRelevant == true"\n`;
  yaml += `#    - Route to Lake: default (catch-all)\n`;
  yaml += `#\n`;
  yaml += `# 3. TUNE: After 24-48h in production, check:\n`;
  yaml += `#    - Are any dropped fields needed? Add them back.\n`;
  yaml += `#    - Are masked fields breaking correlations? Use 'hash'\n`;
  yaml += `#      instead of 'mask' for consistent pseudonymization.\n`;
  yaml += `#    - Is the SIEM still receiving too much volume? Add more\n`;
  yaml += `#      fields to Section 1 or tighten Route filters.\n`;
  yaml += `#\n`;
  yaml += `# 4. GUARD AI (Optional): Enable Cribl Guard's AI model for\n`;
  yaml += `#    unstructured fields (message, description, command_line)\n`;
  yaml += `#    that may contain secrets not caught by pattern rules.\n`;
  yaml += `# ============================================================\n`;

  return yaml;
}

